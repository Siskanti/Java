package belajar.coding;

public class OperatorAritmatika {
    public static void main(String[] args){
        System.out.println("Penjumlahan");
        int sum = 2 + 3;
        System.out.println("1.  2 + 3 = " + sum);
        System.out.println();

        System.out.println("Pengurangan");
        int subtraction = 2 - 3;
        System.out.println("2.  2 - 3 = " + subtraction);
        System.out.println();

        System.out.println("Perkalian");
        int multi = 2 * 3;
        System.out.println("3. 2 * 3 = " + multi);
        System.out.println();

        System.out.println("Pembagian");
        int division = 30 / 2;
        System.out.println("4. 30 / 2 = " + division);
        System.out.println();

        System.out.println("Habis bagi");
        int quotient = 4 % 2;
        System.out.println("5. 4 % 2 = " + quotient);
        int quotients = 3 % 2;
        System.out.println("6. 3 % 2 = " + quotients);
        System.out.println();




    }
}
